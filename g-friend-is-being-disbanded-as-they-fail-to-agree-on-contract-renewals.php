<h2>Here are the letters written by G-Friend Members</h2>
<br>
<h3>
<span style="color:#E60023">UMJI</span><br>

 

"Hello, this is GFriend's Umji.

 

I shared many things that have been a part of my daily life for six years and I'm both thankful and sad.
Now my members and I will go through a new time.<br><br>

 

When I started off, I was filled with a lot of ignorance and lack of knowledge, but it was a time of 
learning that made me realize many things and also reminded me of so many other things. Thank you so much for clapping, 
cheering and being happy for me in this process of growth. At the beginning of our debut, I felt like I was working hard 
without knowing where I was heading or what I was doing. After I had some time to look back, I could feel how hard I worked that 
I was on a great path, so I was able to thank and enjoy all the time and even the hard situations I faced while working.<br><br>

 

Thank you for being the biggest reason for my heart, our fans BUDDY who I love so so much! No matter how hard I 
try to greet you, it hurts me to think of BUDDY who will try to find their hearts to wish us happiness, and I'm sorry 
I can't absorb it all together no matter how hard I try to imagine it. <br><br>

 

I hope that my wish to hug all BUDDY who will be hurt by this unprepared farewell will reach even a little bit. 
Thank you and once again, thank you. I will continue to be thankful always. I think everything's the same, 
but I'm a little scared as I have to adapt to many unfamiliar times now. I'm going to work hard for everyone who looks at me and supports me.<br><br>

 

Thank you so much for all the staff who worked hard for GFriend for the six years. 
Most of all, our precious members who I went through so many things together with. 
If it weren't for you, I wouldn't have experienced everything. Thank you so much. I love you.<br><br>

 

BUDDY, once again I miss you and I love you. I sincerely hope that you'll all be happy and free in the future. Thank you."<br><br>


<span style="color:#E60023">EUNHA</span><br><br>

 

"I think this is upsetting for the fans who were looking forward to us more than ever. I’ve always wondered, 'What kind of pretty words would make BUDDY happy?' Now I'm thinking about what kind of words will comfort BUDDY. I feel heavy writing every single word here.<br><br>

 

I'm so lucky that I can always remember and find the cheering of the fans by my side, who gave me so much strength. 
I wanted to become a better person because you all always gave me so much love and trusted me. But can I be loved like this?
 I'm not as great as BUDDY thinks. Can I enjoy this kind of gift? I've felt this way since my debut. I've felt this way every moment 
 since our promotions, fan meetings, and concerts. All of this was possible because of the fact that you guys supported us. I was really 
 thankful that I could sing good songs,and I was happy to chime in thanks to you who recognized us. It's been the most beautiful and happy 
 six years for me. That fact will never change. I wish all the best for BUDDY who I will love forever. I sincerely hope your lives will be 
 full of smiles rather than tears. Thank you so much for being a reliable BUDDY to GFriend. Always be healthy and happy. BUDDY, I'll pray 
 from the bottom of my heart. Whatever I do and how I look, I'll try my best to sing in the future. I'll do my best to repay all of your fond 
 memories. This has been GFriend. Thank you, and I love you, our BUDDY."<br><br>

 

<span style="color:#E60023">SINB</span><br><br>

 

"Hello. This is GFriend's SinB.<br><br>

 

My heart is heavy delivering this sudden news to BUDDYs with this letter.<br><br>

 

I contemplated for a long time about what to say and was being so careful about the letter that I was leaving words out...<br><br>

 

In the six years promoting as GFriend's SinB, I want to thank everyone who gave stable support for us and because 
they were our motivation, we were able to receive huge amounts of love and support so I was very happy. And I'm also 
thankful to have been able to perform with good songs.<br>

 

When I saw every letter people send, the support, and warm looks, I was able to do better and improve, and with every moment 
I was happy and wanted to share the good things with fans, so I think our past promotions were carried out nicely.<br><br>

 

The fact that I've always told BUDDY that that we'd be together for a while under the name GFriend, that I can't keep 
the promise, makes me so sad and I'm apologetic and regretful. Still, I know the weight of those words, and I will not 
let the sincerity go away.<br><br>

 

We won't forget the fans' love ever, even what we felt during our past promotions.
 We'll remember and keep my thankful heart while slowly repaying you with our individual activities in the future.<br><br>

 

The past six years' memories have been the happiest time, and I wouldn't change it for anything.<br><br>

 

Thank you to Source Music for taking me, who is lacking, into GFriend, and to my members and our BUDDYs,
 the time we spent together feels like a dream, I'm thankful, sorry, and I love you.<br><br>

 

Thank you so much for reading this long letter, and stay healthy until the day we meet again."<br><br>
