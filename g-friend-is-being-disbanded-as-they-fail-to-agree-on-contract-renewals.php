Thank you for protecting me and not letting me break easily though I'm not that strong, 
\and I will show you a better image in the future so as to not end with the sadness of this farewell.<br><br>

 

Once again, I love you, thank you, and I appreciate you.<br><br>

 

This has been GFriend."<br><br>

 

<span style="color:#E60023">YERIN</span><br><br>

 

"Hello, this is GFriend's Yerin.<br><br>

 

It's been a long time since I left a message like this with my own handwriting, but it feels heavy because it's like this.<br><br>

 

I've decided to leave Source Music where I was since the age of 18 to 26. I think it was possible 
because of the family-like members who debuted at the age of 20 and supported me until I became who I am. 
I think it was possible because of BUDDY who gave us so much love and the Source Music family who worked harder than anyone else.<br><br>

 

Will it happen in six years’ time? I've been through all these dreams, and I've always been loved so much that I think it was undeserved.
 I tried to take good care of myself and repay you with great performances.<br><br>

 

I'm sure these times that will never come back have been times I will never forget. No matter what happens, 
thank you for showering me with love. I feel like I was able to come this far because of BUDDY.<br><br>

 

I won't forget this time, and I'll try my best. Thank you for making great memories during my twenties. 
I love you, and I'll love you in the future too.<br><br>
 

Thank you again to the members, BUDDY, and Source Music."<br><br>
