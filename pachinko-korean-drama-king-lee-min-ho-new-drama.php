<h3>A new show starring our beloved King Lee Min-ho has almost completed its filming and we are
all excited about this upcoming drama. This drama is the television adoption of one of the most
famous novels Pachinko, the beloved epic historical novel by Min Jin Lee. The novel was
praised for its depiction of institutional racism, prejudice, and gripping intimacy under a distinct
Asian lens.
</h3>

  
<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 Pachinko details the journey of a Korean family who move to Japan in a period of the country’s
occupation of a then-unified Korea and their involvement in World War II. The story begins in
1883, but the bulk of the novel’s story takes place between 1910 to 1989.<br><br>
The main appeal of Pachinko is Lee’s effortless weaving of individual characters within this
timeline. Its main protagonist is Sunja, whose journey to Japan amid poverty reveals a deep-
rooted undercurrent of intolerance towards Koreans in the country. Her story entwines with the
life of Hansu, a mysterious Korean merchant born into a rich Japanese family, along with a host
of characters that are both Korean and Japanese.


  </h3>
  <br><br>
<h2>Casting and Roles</h2>
<h3>
<a href="https://en.wikipedia.org/wiki/Lee_Min-ho_(actor)" target="_blank">Lee Min-ho</a> will be seen essaying the role of Hansu, an enigmatic outsider and merchant who
has ties to organized crime and embarks on an illicit romance that has far-reaching
consequences.<br><br>
Teenage Sunja will be played by <a href="https://asianwiki.com/Kim_Min-Ha" target="_blank">Kim Min-ha</a>, a heroic young woman who forges her path in a
country that is not her own.<br><br>
<a href="https://en.wikipedia.org/wiki/Jin_Ha" target="_blank">Jin Ha</a> will be playing the role of Solomon, an ambitious and charming young man who be
forced to reckon with his family's past.<br><br>
<a href="https://en.wikipedia.org/wiki/Anna_Sawai" target="_blank">Anna Sawai</a> will be playing the role of a savvy career woman in the male-dominated world of
high finance.<br><br>
<a href="https://www.imdb.com/name/nm1670590/" target="_blank">Soji Arai</a>, who will star as Mosazu, a father who is a successful businessman but worries about
his son's future.<br><br>
<a href="https://en.wikipedia.org/wiki/Kaho_Minami" target="_blank">Kaho Minami</a> will portray the role of Etsuko, a vibrant woman who longs to be reunited with her
estranged daughter.

</h3>
