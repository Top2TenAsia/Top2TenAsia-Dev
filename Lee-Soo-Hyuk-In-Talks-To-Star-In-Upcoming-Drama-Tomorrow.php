<h3>Lee Soo Hyuk had received a casting offer for MBC’s new
drama “Tomorrow” a remake of the original webtoon of the
same name.</h3>

<br><br>
 <h2> Drama Story
 </h2>
 <h3>The series tells the story of long-term job seeker Choi Joon
Woong, who meets a Grim Reaper on a special mission to help
those contemplating taking their own lives. He joins as the
youngest member of their underworld team, which helps save
the lives of those who are thinking of suicide.
  </h3><br>

<h2>Cast
</h2>
<h3>
   Actress  Kim Hee Sun  has been  confirmed  for the role of Goo
Ryun, one of the grim reapers, and  SF9 ’s  Rowoon  is in talks for
the role of Choi Joon Woong.<br><br>
Lee Soo Hyuk is in talks to play the role of Choi Joong Gil, the
team leader of the India Management Team at the Spirit
Management Headquarters.
</h3><br>



