 Encounter (2018),  Record of Youth (2020).
<a href="https://en.wikipedia.org/wiki/Bae_Suzy">Bae Su-ji</a>  better known as Bae Suzy, is a South Korean singer, actress and model. She was a
member of the girl group Miss A under JYP Entertainment. Suzy later made her debut as an
actress with television series Dream High (2011), and has gone on to appear in series such as  Gu
Family Book  (2013), Uncontrollably Fond (2016), While You Were
Sleeping (2017), Vagabond (2019) and Start Up (2020).
