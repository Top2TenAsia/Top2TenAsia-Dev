<h2 style="text-decoration:underline">Synopsis</h2>
<h3>
  "Flower of Evil" is a thriller drama that is full of suspenses. The story is basically about a psychopath named Baek Hee Sung who changes his identity in order to conceal his cruel past in order to live a happy and peaceful life and his homicide detective wife named Cha Ji Won who tries to track down the truth about her husband.<br><br>

Our beloved Lee Joon Gi will play the role of Baek Hee Sung who on the outside looks like an ideal  and lovely husband who is a metal craftsman. Through his metal craft studio, he makes enough money to provide for his wife and daughter and show his dedication as an exemplary husband and father. He's comfortable with housework and childcare and is known as a man with no greed, being satisfied with what he already has. But this perfect exterior behavior hides some very dark secrets that he is hiding from everyone.<br><br>

Our beautiful and lovely Moon Chae Won will play the wife of Baek Hee Sung, she is a homicide detective known as Cha Ji Won. it's Cha Ji Won's job to uncover as many secrets as she can, in her never-ending quest for the truth. With an insatiable curiosity and an unwavering determination to solve even the hardest cases, Ji Won lives for her work and the excitement she receives from solving crimes which gives her high satisfaction with her career.<br>
