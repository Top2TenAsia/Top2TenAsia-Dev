                <p>Global K-pop powerhouse BTS has officially returned with their fifth studio album <strong>&quot;ARIRANG,&quot;</strong> released on March 20, 2026&mdash;and the numbers are already historic.</p>

                <p>According to data from BigHit Music and Hanteo Chart, ARIRANG recorded approximately <strong>3.98 million copies</strong> sold within its first 24 hours (March 20&ndash;21). This marks the highest first-day sales in BTS&apos;s career, surpassing their previous record of 3.37 million set by Map of the Soul: 7 in 2020.</p>

                <p>The album also crossed 1 million sales within minutes of release, reflecting the immense anticipation surrounding BTS&apos;s first full-group comeback after completing mandatory military service.</p>

                <h3>Record-Breaking Sales Performance</h3>
                <ul>
                    <li>3,981,507 copies sold on Day 1, according to Hanteo Chart</li>
                    <li>Surpassed BTS&apos;s previous first-week record within a single day</li>
                    <li>Reached 1 million sales in roughly 10 minutes after release</li>
                    <li>Becomes BTS&apos;s fastest-selling album to date</li>
                </ul>

                <p>While SEVENTEEN&apos;s FML still holds the all-time highest first-day sales record in K-pop (approximately 3.99 million), ARIRANG now ranks among the top-performing album debuts in industry history, highlighting BTS&apos;s continued commercial dominance.</p>

                <h3>Global Chart Domination</h3>
                <p>Alongside strong physical sales, BTS achieved immediate success across streaming and global charts.</p>

                <p><strong>In South Korea:</strong></p>
                <ul>
                    <li>Title track &quot;Swim&quot; reached No. 1 on Melon, Bugs, Genie, and FLO</li>
                    <li>On Melon&apos;s Top 100, &quot;Swim&quot; debuted at No. 1, followed by &quot;Body to Body&quot; at No. 2</li>
                    <li>All 14 tracks from the album entered Melon&apos;s Top 100</li>
                </ul>

                <p><strong>Internationally:</strong></p>
                <ul>
                    <li>ARIRANG topped iTunes Top Albums in 80+ countries</li>
                    <li>&quot;Swim&quot; reached No. 1 on iTunes Top Songs in 90 countries, including the US, UK, Japan, Germany, and France</li>
                    <li>The music video for &quot;Swim&quot; trended at No. 1 on YouTube in dozens of countries</li>
                </ul>

                <p>These results reinforce BTS&apos;s position as one of the most globally influential music acts.</p>

                <h3>Album Concept and Meaning</h3>
                <p>ARIRANG marks BTS&apos;s first full-group album in nearly four years, making it a highly anticipated release among fans worldwide.</p>

                <p>The album draws inspiration from the traditional Korean folk song &quot;Arirang,&quot; often considered a cultural symbol of Korea. Elements of this theme are incorporated into the opening track &quot;Body to Body.&quot;</p>

                <p>According to BigHit Music, the album features 14 tracks that reflect:</p>
                <ul>
                    <li>BTS&apos;s personal journey</li>
                    <li>Themes of growth, resilience, and identity</li>
                    <li>A message of gratitude toward their fanbase, ARMY</li>
                </ul>

                <p>The lead single &quot;Swim&quot; represents perseverance and moving forward despite challenges.</p>

                <h3>BTS Comeback Event Streamed Globally</h3>
                <p>To celebrate the release, BTS hosted <strong>&quot;BTS The Comeback Live: ARIRANG&quot;</strong> on March 21, 2026, at Gwanghwamun Square in Seoul.</p>

                <p>The event marked their first full-group live performance in years and was streamed globally via <strong>Netflix</strong>, allowing fans worldwide to participate in the comeback moment.</p>

                <p>The concert featured:</p>
                <ul>
                    <li>Performances of new tracks including &quot;Swim&quot;</li>
                    <li>Large-scale production and choreography</li>
                    <li>Visual storytelling tied to the album&apos;s themes</li>
                </ul>

                <h3>Key Takeaways</h3>
                <ul>
                    <li>ARIRANG sold ~3.98 million copies on Day 1, BTS&apos;s highest ever</li>
                    <li>The album achieved immediate global chart success, topping iTunes and Korean platforms</li>
                    <li>All 14 tracks entered Melon Top 100 upon release</li>
                    <li>Marks BTS&apos;s first full-group comeback after military service</li>
                    <li>Celebrated with a global livestream concert on Netflix</li>
                </ul>

                <h3>Final Note</h3>
                <p>With several days still remaining in its first-week tracking period, ARIRANG is expected to further extend its sales record, potentially setting new benchmarks in K-pop.</p>

                <p>BTS&apos;s latest release not only demonstrates their enduring global popularity but also signals a powerful new chapter following their long-awaited return.</p>

