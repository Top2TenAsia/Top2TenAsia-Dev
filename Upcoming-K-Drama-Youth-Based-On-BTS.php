  <h3>World famous South Korean boy band BTS attracts
millions of hearts with their never ending charm, hard work
and talent. According to sources an upcoming drama
“Youth” is going to be based on BTS universe. Army must
be super excited as they’ll see more about BTS on screen.<br>
</h3>

<h2>Drama Story</h2>
<h3>
The series is going to be based on the BTS Universe, it will
be a fictional story about BTS lives. It will also feature how they
pursue their dreams as IDOL. “Youth” will be showing the storyline
fans have seen throughout BTS&#39; music videos and “Save Me”
webtoon. The story will begin with Seok-jin meeting the six other
guys. He learns everyone is dealing with their own struggles, and
together, they learn to overcome their greatest difficulties. The
story is going to be fictional but still we will be able to see real
struggle of any band.</h3><br>

<h2>Cast and Roles</h2>
<h3>
    The characters are the alter egos of the band’s seven members and each of them are named after real names of BTS band members. The role of kim seok-jin will be played by seo ji-hoon. Noh jong-hyun will be shown as min yoon-gi, ahn ji-ho as jung ho seok, seo young-joo as kim nam joon(leader of BTS), kim yoon-woo as park ji-min, jung woo-jin as kim tae-hyung and jeon jin-seo will be playing the role of most adorable and youngest member of BTS Jung-kook.

</h3><br><br>


<h2>Writer and Director</h2>
<h3>The upcoming drama “YOUTH” will be directed by Kim
Jae-Hong and written by Kim Soo-Jin, Choi Woo-Joo.</h3><br>

<h2>Filming and release</h2>
<h3>
Filming was halted in late October 2020 following concerns raised
by fans about the characters&#39; names being the same as the band
members&#39; names. Originally scheduled to resume in early
December, filming was eventually pushed back to January 2021.
According to sources the series is set to be released in 2021.</h3>
