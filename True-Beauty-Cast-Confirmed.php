<h2 style="color:red;text-decoration:underline"> Casting</h2>
<br>
<img src="chae.webp" class="img-thumbnail">
<br><h3>
Astro's <span style="color: dodgerblue">Cha Eun-woo</span> will lead the cast and could be seen as the character of <span style="color: dodgerblue">Lee Suho</span> who have a lot of merits like good looks, good grades and a beautiful heart. Lot of people like him but he didn't pay attention to them. He didn't like to talk much. Everyone thoughts he's cold but in reality he's warm hearted but shows his coldness only.
</h3>
<br>
<img src="moon1.webp" class="img-thumbnail">
<br>
<h3>
<span style="color: dodgerblue">Moon Ga Young</span> portrays the character of <span style="color: dodgerblue">Lim Ju Gyeong</span> who's also a high grade student. She's so popular in her school and all credits to her makeup skills. She would chose to die rather than revealing her bare face. She becomes the most beautiful woman in her school but just with her makeup on. There's a large difference between her bare face and makeup looks.
</h3>
<br>
<h3>
<span style="color: dodgerblue">Hwang In Yeob</span> portrays the character of
<span style="color: dodgerblue">Han Seo Jun</span> who's also handsome but in another way of Suho. He got entangled with Gyeong but later he falls in love for her. He will be the second lead of the drama.
</h3>
<br>
<h2 style="color:red;text-decoration:underline">Story Plot</h2>
<br>
<h3>
This drama tells the story of a girl who does so much makeup to look beautiful and a boy who has scar on his heart and behaves like a cold hearted man. There will be love triangle shown in this drama.
</h3>
<br>
<h3>
 <span style="color: dodgerblue">Kim Sang Hyub</span> will direct this drama and this is adapted from a webtoon of same name written by <span style="color: dodgerblue">Lee Si Eun</span>.
Now casting is done and shooting will start soon. This drama is expected to hit the screen in the second half of 2020.

