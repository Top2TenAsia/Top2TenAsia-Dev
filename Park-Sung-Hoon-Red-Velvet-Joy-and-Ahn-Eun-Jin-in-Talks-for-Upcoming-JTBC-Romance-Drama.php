  <h3>We may be seeing Ahn Eun Jin and Park Sung Hoon in a new drama soon. It will be named as
‘Just as person’.</h3>
<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 Just One Person” is a romance drama about a terminally ill woman and a man with a very
difficult life who gets involved with each other due to a murder case and become the most
valuable “one person” in each other’s lives. It will be very interesting to watch this. Ahn Eun Jin
will play Pyo In Sook, a terminally ill character with an insensitive personality who has never
belonged anywhere. According to previous reports, Park Sung Hoon will play Seoul
Metropolitan Police Agency inspector Jo Si Young, and Joy will play famous influencer Sung
Mi Do.
  </h3><br>
  <h2> Drama Cast
 </h2>
 <h3>
  Ahn Eun Jin made her debut as an actress in the theater in 2011. Since then, she has appeared in
a number of theater productions, films, and television dramas, including “The Crowned Clown”
(2019), “My Fellow Citizens!” (2019), “War of Prosecutors” (2019), and “More than Friends”
(2020).
Park Sung Hoon made his debut in the entertainment world in 2006. Since debut, he has
appeared in various films and television dramas, including “Rich Man, Poor Man” (2018), “My
Only One” (2019), and “Justice” (2019).
Red Velvet’s Joy, Joy is the stage name of Park Soo Young, a South Korean rapper and vocalist
who is best known for being a member of the girl group Red Velvet. Born on September 3, 1996,
Joy is the lead rapper and sub-vocalist for Red Velvet, which debuted in 2014. Joy’s fame also
rose when she was paired with BTOB’s Yook Sung Jae in the popular reality series “We Got
Married Season 4” in 2015. After making her acting debut in the 2017 television drama “The
Liar and His Lover,” Joy also starred in the drama “Tempted” (2018).
</h3>
<br>

<h2>Director and Screen writer
</h2>
<h3>
The drama will be written by Moon Jung Min of “Matrimonial Chaos” and directed by Oh Hyun
Jong of “Weightlifting Fairy Kim Bok Joo,” “Two Cops,” and “Find Me in Your Memory.” It
aims to begin filming in July and premiere in December via JTBC.

