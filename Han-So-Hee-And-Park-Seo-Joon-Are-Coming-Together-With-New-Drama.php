<h3> Han So Hee in talks along with Park Seo Joon for their
new upcoming drama. Our favourite Park seo joon who is
famous for his roles in many dramas like “What’s wrong
with secretary kim”, “Fight for my way”, “Hwarang”,
Itaewan class” and many more dramas , is now coming
with his new drama and share the screen with very
beautiful Han So Hee. She is famous for her work in many
dramas like “Money flower”, “100 days my prince” and
gained wide recognition for her drama “The world of
married”. Now they are coming together with their new
drama “Gyeongseong creature”.</h3>
<br><br>
  
<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 The new drama is expected to be a thriller drama which is
about survival and human dignity. The story is about youth
who lived during a time when sovereignity and human
rights were taken away from them and they had to put
their lives on the line just to achieve the most basic
happiness. The story sounds amazing with great storyline.
  </h3>

<h2>Directors and Screenwriters
</h2>
<h3>
The drama is going to be directed by Jung Dong Yoon
who also directed the series “Stove league”. The drama

will be written by Kang Eun Kyung who has also written
the series “Dr. Romantic”
According to sources Han so hee’s agency revealed
that she got the offer for main lead in upcoming drama
“Gyeongseong creature” and actor Park Seo Joon had
also got offer to play main lead role in the above
mentioned drama. The drama is going to be a big hit with
a lot of hard work of the amazing cast, stay tuned for more
details.
</h3>


