<h3> As per Jin Ki Joo Confirmed To Join Park Hae Jin In Upcoming
Fantasy Rom-Com About Ghosts.
After making her debut by the tvN drama “Second 20s” in 2015,
Jin Ki Joo starred within the movies “Little Forest” and “Midnight”
together with the dramas “Wednesday 3:30 PM,” “Moon Lovers:
Scarlet Heart Ryeo,” “Come and Hug Me,” “The Secret Life of My
Secretary,” and “Homemade Love Story.” Widely acknowledged
for her performing expertise, Jin Ki Joo has been busy with
promotions for her newest movie “Midnight” after wrapping up
“Homemade Love Story” in March.
Jin Ki Joo’s newest undertaking “From Now On, Showtime!” will
mark her first time working with Park Hae Jin.</h3>

<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 “From Now On, Showtime!” is a fantasy rom-com about a magician and a
female police officer. A charismatic magician who also has the
ability to see and manipulate ghosts. The drama is going to be a
comical romance investigative drama in which the characters work together
with ghosts. In addition to the romance between Cha Cha Woong, a
magician and employer who handles and manages ghosts, and a
passionate female.
  </h3><br>

<h2>Cast and Roles
</h2>
<h3>
The drama features Park Hae-jin playing the role of a famous
magician named Cha Cha-woong. He is described as being
supercilious and stubborn, and as someone with nothing to fear.
Interestingly, he utilizes his ability to see and interact with ghosts,
and actually employs them to do his bidding. It’s unclear if the
ghosts are the secret behind his magic tricks, or if Park actually
has some kind of magical power.<br><br>
Jin Ki-joo will be playing the role of police officer Go Seul-hae who
works at the local police station. Considerate towards other
people and passionate for justice, her goal is to one day become
a detective. Naturally, she is also hiding a secret, and somehow
gets entangled with Park Hae-jin when she is assigned to a new
case
</h3><br>

<h2>Directors and Writers
</h2>
<h3>
The drama is going to be directed by PD Lee Hyung-min who had
also directed My Dangerous Wife, Chocolate and written by Ha
Yoon-ah who had also written the drama Mystic pop-up bar.
</h3><br>


<h2>Filming and Release
</h2>
<h3>
The drama is at present in its closing phases of casting, and it’s
scheduled to premiere within the first half of 2022. The drama is
going to be exciting and impacting to the viewers.
</h3>
