  <h3>2PM’s <a href="https://en.wikipedia.org/wiki/Lee_Jun-ho_(entertainer)">Junho</a> and <a href="https://en.wikipedia.org/wiki/Lee_Se-young">Lee Se Young</a> are confirmed to star in the upcoming MBC historical melodrama <span style="color:coral">“The Red Sleeve Cuff”</span>.</h3>
<br>
 <h2> Drama Story
 </h2>
 <h3>
 <span style="color:coral">“The Red Sleeve Cuff”</span> will be a record of an imperial court romance between a court lady,
  who wanted to protect the life she has chosen, and an emperor, who put the nation first before love. <span style="color:coral">“The Red Sleeve Cuff,”</span>
   which is based on a novel of the same name writer Kang Mi-gang. PM’s Junho will play Yi San, the grandson of the king preparing 
   to himself be king one day. He is stubborn, arrogant and a perfectionist. He constantly strives to become a great and wise king but 
   his father’s death has ;eft him traumatized. However, when he meets court lady Seong Deok Im, he starts to soften and show a romantic 
   side that even he did not know that he had.

  </h3><br>
  <h2>Drama Cast
</h2>
 <h3> Through the dramas <span style="color:coral">“Just Between Lovers”</span>, <span style="color:coral">“Wok of Love”</span>, <span style="color:coral">“Confession”</span>, and <span style="color:coral">“Good Manager”</span>, 
 Junho received praise for his stable acting and ability to take on any genre. “The Red Sleeve Cuff” 
 will be his first project since his discharge from his mandatory service in March.
Lee Se Young will be playing court lady Seong Deok Im, who wants to independently make decisions about her 
life rather than be one of the King’s many women. The actress has already impressed viewers with numerous projects including 
<span style="color:coral">“The Crowned Clown”</span>, <span style="color:coral">“Doctor John”</span>, <span style="color:coral">“Memorist”</span>, and <span style="color:coral">“Kairos”</span>. In her most recent project “Kairos,” Lee Se Young made a shocking 
transformation with her new short hairstyle, leaving a strong impression on viewers.
</h3>
<h2>Director and Screen writer
</h2>
<h3>
The drama is directed by Jung Ji In, who worked on <span style="color:coral">“Radiant Office”</span> and <span style="color:coral">“Hold Me Tight”</span>
and it will be written by scriptwriter Jung Hae Ri, who worked on <span style="color:coral">“Ruler: Master of the
Mask”</span>. It will be fun to watch this historical drma with amazing actors.
</h3>
