   <h3> Talking about the title DREAM , it itself says about the ambition or aspiration of the protagonist. Who wants to make his/her dream comes true and can do anything to achieve it , no matter what . <a href="https://en.wikipedia.org/wiki/Lee_Byung-hun">"Lee Byung Hun"</a> who is one of the well known name in the south korean movie and drama Industry  and the one who direct the 2019 movie <a href="https://en.wikipedia.org/wiki/Extreme_Job">Extrem job</a> is directing Dream. The shooting of “DREAM”  has already begun since 7th May , 2020 and expected to hit the screen in 2021 .
  </h3>
