 <h2> About Drama
 </h2>
 <h3>
    Delivery Knight is a Netflix original drama series that is a
webtoon adaptation with the same title. The story follows a
dystopian era in the future — a period where pollution is
getting worse. Therefore, the only way to survive is to find
necessities through delivery.<br><br>
According to sources on July 2, the agency AM
Entertainment announced that Kim Woo Bin has been
offered to star in the Netflix original drama series titled
“Delivery Knight” (working title). Moreover, he is currently
in talks to appear in it.<br><br>
If he accepts the offer then fans could see their loving
actor in dramas again after five years and play legendary
role although he is still reviewing the offer and it’s not
confirmed yet if he’ll accept the offer or not.<br><br>

He was absent for a long time in dramas due to two years
of hiatus due to undergoing treatment for nasopharyngeal
cancer. Then, at the end of 2019, he made a comeback in
good health to the entertainment world.<br><br>
After that he has been active in many photoshoots for
various magazines. Moreover, he recently finished filming
his upcoming movie titled “Alien.” In addition, Kim Woo Bin
is also considering another Korean drama offer titled “Our
Blues.”<br><br>
Fans are too excited to watch him on screen again with
some legendary come back and this drama is best for that
hope he’ll accept it and we can see him doing some role
doing justice to his looks and personality.<br><br>
