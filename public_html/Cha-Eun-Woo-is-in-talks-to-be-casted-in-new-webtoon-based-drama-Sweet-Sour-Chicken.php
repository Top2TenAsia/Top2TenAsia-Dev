    top” and many more. And now he is coming with another
    drama, according to sources this time he is going to play a role
    he has’nt played yet. He is going to play a totally different
    character he has ever played.</h3>

<br><br>
 <h2> Drama Story
 </h2>
 <h3>
    It tells the strange story of how a beautiful young girl,
    Choi Min Ah, turns into fried chicken because she
    happens to enter a mysterious machine. It follows Choi
    Sun Man, her father, and Go Baek Jung, an intern, who
    join efforts to turn her back to human and discover
    deep dark secrets in the process.
  </h3><br>

<h2>Cast
</h2>
<h3>
    Cha Eun Woo is currently in talks to be casted as main
    lead of the drama “sweet and sour chicken”. Other cast is
    yet to be confirmed.
</h3><br>

<h2>Directors and Writers
</h2>
<h3>
    Lee Byun Heon is going to direct and write this upcoming
    drama “Sweet and Sour Chicken”, who had already shown
    his directing skills in the movie “Extreme Job”.
</h3><br>


