<p style="float: right;"> By <a href="https://www.instagram.com/_.bisht._/" target="blank" style="color:coral;">Prachi Bisht</a></p><br>
<br>
<br>
<h3>
  <a href="https://en.wikipedia.org/wiki/Ji_Chang-wook">Ji Chang Wook</a>'s fans are still enjoying his ongoing drama, <a href="https://www.top2ten.xyz/backstreet-rookie">Backstreet Rookie</a>, and another good news has come which is connected to him. <span style="color: dodgerblue">Ji Chang Wook</span> can soon be seen in a new drama, that is titled as <strong>Kakao TV's</strong> <span style="color: violet">"City couple's way of love"</span>.
  <br>
It is his grace or elegance that he cannot keep his loved ones away from him for long.
</h3>
<h3>
Talking about <span style="color: dodgerblue">Ji Chang Wook</span> new project, it is yet not confirmed as he is in talk with <a href="https://en.wikipedia.org/wiki/Kim_Ji-won_(actress)">Kim Ji Won</a> for this project. It will be <span style="color: dodgerblue">Jiwonie's</span> comeback like drama. The audience gave her a lot of love for her drama, <a href="https://www3.dramacool.movie/drama-detail/third-rate-my-way">Fight my way</a>.
</h3>
<h3>
In response to this report, both actor's agency, that is <span style="color: dodgerblue">Glorious Entertainment</span> and <span style="color: dodgerblue">SALT Entertainment</span> disclosed the news that the script had been discussed with the actors and will reviewing in it.
</h3>
<h2 style="color:red;text-decoration:underline">About Drama</h3>
<h3>
<hr>
The <span style="color: violet">City couple's way of love</span> will be directed by <a href="http://asianwiki.com/Park_Shin-Woo_(director)">Park Shin Woo</a>, the same PD(Producing Director) of the most romantic yet thriller ongoing drama <a href="https://www.top2ten.xyz/it's-okay-to-not-be-okay">It's okay to not be okay</a>. <a href="http://asianwiki.com/Jung_Hyun-Jung_-_screenwriter">Jung Hyun Jung</a> is the screenwriter of the drama who had written <a href="https://www3.dramacool.movie/drama-detail/i-need-romance">I need Romance</a> and <a href="https://www3.dramacool.movie/drama-detail/discovery-of-romance">Discovery of Love</a>.
</h3>
<h3>
It will be of 12 episodes and each episode will run about the duration of 25 min. <em>It tells the love story that covers the gap of sensitivity between the two cities</em>.
<br>
The filming of the drama may start from October and will release in November of 2020 on <strong>Kakao TV network</strong>.
<hr>
</h3>
<br>
<h3>
Right now, all the viewers in the country or abroad are very fond of his Ongoing drama, <a href="https://www.top2ten.xyz/backstreet-rookie">Backstreet Rookie</a>. Earlier, this drama was surrounded by controversy about how such an age gap could occur. But now people are enjoying that drama very much. 12 episodes have already arrived 4 are due.<br> <span style="color: dodgerblue">Ji Chang Wook</span> and <a href="https://en.wikipedia.org/wiki/Kim_Yoo-jung"></a>Kim Yoo Jung's life has seen so much turmoil in these episodes. After <span style="color: dodgerblue">Ji Chang Wook</span>'s heartbreak, how <span style="color: dodgerblue">Kim Yoo Jung</span> handled him, was overwhelming the audiences. The viewers are liking their pair a lot. Now people are giving loads of love to this drama.
</h3>
