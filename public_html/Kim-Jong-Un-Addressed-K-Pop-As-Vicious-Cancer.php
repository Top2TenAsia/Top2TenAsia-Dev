 <h3>

 The supreme leader of North Korea Kim Jong-un has
recently described k-pop music as “vicious cancer”,
corrupting the younger generation of the country.
According to New York Times Kim Jong has declared
a new cultural war to stop the craze of South Korean
music and movies (includes k-dramas and k-pop videos)
among his citizens through a secretive anti k-pop
campaign.<br><br>
According to the sources the new culture war was
leaked internal documents from the Democratic People’s
Republic of Korea. In order to stop the public to get
influenced by South Korean music and movies Kim Jong
introduced plenty of new laws that increased the level of
punishment for watching or owning South Korean
Entertainment.<br><br>
As per the sources, if someone will watch any South
Korean drama or videos it will result in 15 years of jail in
hard labor camp.<br><br>
The news is quite depressing for North Korean fans
those love to watch South Korean dramas and love to
hear k-pop like all of us. But according to Kim Jong-un the
youth is loosening its grip after getting influenced by South
Korean k-pop and dramas and he plans to impose worse
punishments for those found guilty which may include
death penalty as well.<br><br>
According to Kim Jong South Korean culture is
influencing their youth for their attire, hairstyle, speeches
and behavior.<br><br>

South Korean music, culture and dramas are
influencing young North Koreans which made Kim Jong-
un uneasy and that’s the reason he is trying to stop them
by imposing many laws and punishments against them.

