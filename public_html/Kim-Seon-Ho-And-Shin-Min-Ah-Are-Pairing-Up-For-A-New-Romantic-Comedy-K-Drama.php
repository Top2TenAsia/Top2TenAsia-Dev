<h3> Kim Seon Ho and Shin Min Ah are pairing up for a new romantic comedy K-
drama ‘&quot;Seashore Village Cha Cha Cha&quot;<br>
TvN has confirmed that the Start-Up alum will star alongside Shin Min Ah for the
network’s upcoming drama titled Seashore Village Cha Cha Cha.</h3>
<br><br>
  
<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 Based on the 2004 South Korean romantic comedy film, Mr. Hong, this upcoming drama follows
Yoon Hye Jin (Shin Min Ah), a picture-perfect woman who works as a well-respected dentist.
Her seemingly ideal life falls to shambles when she loses her job, leading her to move to the
seaside village of Gongjin. In the midst of attempting to restart her career, she crosses paths with,
and eventually develops a fondness for, Hong Doo Shik (Kim Seon Ho), who’s popularly known
in the area as Mr. Hong. Although unemployed, Mr. Hong is a jack of all trades and can take on
any odd job if it means helping out the locals of Gongjin.
  </h3>

<h2>Cast
</h2>
<h3>
Kim Seon-ho is a South Korean actor. He began his career on stage and appeared in numerous
plays before making his screen debut in 2017 with Good Manager. He rose to prominence with
the 2020 television series Start-Up.<br>

Shin Min-ah is a South Korean model and actress. She is best known for starring in television
dramas A Love to Kill (2005), My Girlfriend Is a Nine-Tailed Fox (2010),  Arang and the
Magistrate  (2012), Oh My Venus (2015), and Tomorrow, With You (2017).<br>

Described as the “perfect rom-com lineup,” the pair is expected to demonstrate an irresistible
kind of chemistry, as per Seashore Village Cha Cha Cha’s production team. “Not only will Shin
Min Ah and Kim Seon Ho show surprisingly high synchronization with their characters, but the
bubbly yet warm story will also provide comfort,” they further said in a statement. “Please look
forward to their rom-com chemistry that is bound to make hearts flutter.”
</h3>


<h2>Directors and Screenwriters
</h2>
<h3>
The drama is directed by Yoo Je Won. You’re most probably familiar with his work on famous K-
dramas like The King: Eternal Monarch and Hi Bye, Mama. He’s also previously worked with
Min Ah on her 2017 romantic drama, Tomorrow with You. Meanwhile, penning the script is Shin

Ha Eun, whose best credit to date involves co-writing the screenplay for the historical romance
series The Crowed Clown.
</h3>
