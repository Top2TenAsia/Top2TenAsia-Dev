<h3> 
<strong>A new exciting tvN network Korean drama, The Moment, is coming soon on Netflix. 
We will give you all the information related to this drama such as release date, casting and synopsis</strong>.
<br>
</h3>
<h3>
    <em>The Korean drama, which was earlier named as <span style="color: violet">Record of youth</span>, has now been renamed to <span style="color: dodgerblue">The Moment</span>.
    	<br>
    <span style="color: dodgerblue">The Moment</span> is an original tvN drama screenwritten by <span style="color: dodgerblue">Ha Myung Hee</span> and directed by <span style="color: dodgerblue">Ahn Gil Ho</span></em>.   
</h3>
<br>
<h3>
	According to our search and reports, this drama may be streaming on <strong>Netflix</strong> in September. It is anticipated that the drama will run from <em>September 7</em> to <em>October 27</em>. Its episodes will be streaming on <em>Monday</em> and <em>Tuesday</em>. Like many Korean drama, this drama will have 16 episodes.
</h3>
<br>
<h2 style="color:red; text-decoration:underline">
Cast:
</h2>
<br>
<h3>
	<a href="https://en.wikipedia.org/wiki/Park_Bo-gum">Park Bo Gum</a> will be seen in the lead role in this drama. Whom we have been seen many times before in many biggies like <a href="https://www3.dramacool.movie/drama-detail/itaewon-class">Itaewon Class</a> (In this drama, he was seen in the appearance of a guest role), <a href="https://www3.dramacool.movie/drama-detail/reply-1988">Reply 1988</a> (He played the role of Choi Taek in main role as one of the five childhood friends), <a href="https://www3.dramacool.movie/drama-detail/hello-monster">Hello monster</a> and<a href="https://www3.dramacool.movie/drama-detail/moonlight-drawn-by-clouds"> Love in the moonlight</a>.
</h3>
<br>
<h3>
	
He will be seen opposite to<a href="https://en.wikipedia.org/wiki/Park_So-dam"> Park So Dam</a>,  she has been seen in the film<a href="https://en.wikipedia.org/wiki/Parasite_(2019_film)"> Parasite</a>. Her movie was premiered at the <em><strong>Cannes Film Festival</strong></em> in 2019 where it became the first South Korean film to won the<a href="https://en.wikipedia.org/wiki/Palme_d%27Or"> Palme d'Or</a>. This film received so many awards and also became the highest grossing film of the year. 
</h3>
<br>
<h3>
<a href="http://asianwiki.com/Byeon_Woo-Seok">Byun Woo Suk</a> will also seen in lead role. He has been seen in mega hit <a href="https://www3.dramacool.movie/drama-detail/www">Search: WWW</a> in support role.
</h3>
<br>
<h3> 
<span style="color: dodgerblue">Shin Ae Ra</span> and <span style="color: dodgerblue">Ha Hee Ra</span> will also seen in this drama.

</h3>

<br>
<h2 style="color:red; text-decoration:underline">
Synopsis:
</h2>
<br>
<h3>
This drama shows the lives of 3 young people of fashion industry. Your destiny dictates which path you want to follow in life. Today's generation lives in a world where the you are born with a big name determines the future of your life. Luxurious dream is not allowed to all people to have. If they does not have the right to have luxurious dream, still they can achieve whatever they are denied to do. This drama wants to tell and teach us this.
</h3>
<br>
<h3>
	<span style="color: dodgerblue">Park Bo Gum</span> will portrays the character of <span style="color: violet">Sa Hye Joon</span>.
	<br>
