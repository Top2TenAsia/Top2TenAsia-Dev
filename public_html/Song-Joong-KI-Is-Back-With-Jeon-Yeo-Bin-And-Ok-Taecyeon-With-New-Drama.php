<h2>
<a href="https://en.wikipedia.org/wiki/Song_Joong-ki">Song Joong Ki</a> is making is making comeback on small screen along with <a href="https://en.wikipedia.org/wiki/Jeon_Yeo-been">Jeon Yeo Bin</a>, and  <a href="https://en.wikipedia.org/wiki/Ok_Taec-yeon">Ok Taecyeon</a> from <a href="https://en.wikipedia.org/wiki/Hey_Ghost,_Let%27s_Fight">Hey Ghost, Let's Fight</a>.<br><br>
</h2>
<h2 style="text-decoration: underline;">Drama Story</h2>
<h3>
The Drama is entitled as <span style="color: coral">“Vincenzo”</span> which tells a story about an Italian Lawyer and Mafia Vincenzo Cassano, who got involved in the a conflict within his organization and decide to move to South Korea. There he got involved with the tough lawyer known as Hong Cha Young. Together with joining forces they bring justice to the people in need and punishes the villans.
</h3>
<h2 style="text-decoration: underline;">Casting and Roles</h2>
<h3>
Song Joong Ki will play a role of a role of Italian Lawyer and Mafia Vincenzo Cassano who is Korean descent but got adopted by the Italian family and live in Italy from a young age. He have a exceptional skills and charisma that no one can neglect. He moves to Korea due to his conflict between his organisatin and there he start to uncover the deep rooted truths and join forces with Hong Cha Young.<br><br>

Hwang In Yeob will Play a role of Hong Cha Young who will do anything to solve a case. She is the ace lawyer at South Korea  law firm known as  Woosang. She is really curious lawyer she focuses on what is the situation of person  advantageous or disadvantageous related to case rather then he/she is bad or good person but after meeting Vincenzo Cassano she slowly started to change.<br><br>

Ok Taecyeon, will play a role Jang Jun Woo, a really handsome and intelligent intern at a well known firm. He has a experience living and working abroad. He has a carefree smile with slightly clumsy and odd personality. He really have trust and faith in Hong Cha Young who is his senior at work, and when Vincenzo Cassano get involved with them his competitive spirit get triggered.
</h3>
<h2 style="text-decoration: underline;">Director & Screenwriter</h2>
<h3>“Vincenzo” is directed by Kim Hee Won who is well known for his drama <a href="https://en.wikipedia.org/wiki/Money_Flower">“Money Flower”</a>  and <a href="https://en.wikipedia.org/wiki/The_Crowned_Clown">“The Crowned Clown”</a>  while the screenwriter of the drama is Park Jae Bum, who is well known for writing <a href="https://en.wikipedia.org/wiki/Good_Manager">“Good Manager”</a> and <a href="https://en.wikipedia.org/wiki/Priest_(TV_series)"> “The Fiery Priest”</a>.</h3><br><br>
<h3>
  “Vincenzo” will air on February 20, 2021 having total 16 episodes. It's have really high expectation among the Korean Drama lovers because of huge star cast and well known Director & Screenwriter.
</h3>
