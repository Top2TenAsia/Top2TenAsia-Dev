  <h3>After the successful conclusion of “Vincenzo”, Korean heartthrob Song Joong Ki is
reportedly in talks to star with Suzy Bae in an upcoming drama entitled “The Youngest Son
of Chaebol Family.”</h3>
<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 “The Youngest Son of Chaebol Family” is the live action adaptation of a webtoon
“Youngest Son of a Congomerate. The drama centers around a man who was
wrongfully accused and eliminated by a chaebol clan after he showed loyalty to the
family. In an unexpected twist, he was reborn as the youngest son of the same
family. It later becomes his mission to take them down for what they have done in
his past life.
  </h3>
<h2>Casting and Roles</h2>
<h3>
Song Joong Ki rose to fame in the historical coming-of-age drama Sungkyunkwan
Scandal (2010) and the variety show Running Man (2010–2011) as one of the
original cast members. Since then, he has played a diverse spectrum of roles in the
television series The Innocent Man (2012), Descendants of the Sun (2016), Arthdal
Chronicles (2019) and Vincenzo (2021), as well as the hit films A Werewolf
Boy (2012)<br><br>
Bae Suzy, is a South Korean singer, actress and model. She was a member of
the girl group Miss A under JYP Entertainment. Suzy later made her debut as an
actress with television series Dream High (2011), and has gone on to appear in series
such as Gu Family Book (2013), Uncontrollably Fond (2016), While You Were
Sleeping (2017), Vagabond (2019) and Start Up (2020)<br><br>
<br><br>

</h3>

