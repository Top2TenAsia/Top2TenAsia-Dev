<h2 style="text-decoration:underline">Synopsis</h2>
<h3>
Startup as we can guess from the title it's basically based on the story of people who want to start up their own business.

It is a story of two entrepreneurs who have a dream and want to get success with their own start-up companies. Seo Dal-mi (Bae Suzy), a second-generation heiress who drops out of the college just like the Facebook owner Mark Zuckerberg to start her own company and be as successful as the famous entrepreneurs like Bill Gates, Steve Jobs, Mark Zuckerberg, etc. She also gives up her all privileges and takes various jobs to raise the money she can use as capital. She wants to create her startup from the scratch.<br><br>

On the other side Nam Do-san, the founder and CEO of a failing start-up company. He excelled at Math as a child and a genious who constantly made his family proud, but since running his own company two years ago, life has become difficult for him. He is working hard to make his startup company successful.

When one day Nam Do-san suddenly meet up with Seo Dal-mi, the two become good friends and offer each other support as they try to launch a next and new big start-up that will make them successful.
