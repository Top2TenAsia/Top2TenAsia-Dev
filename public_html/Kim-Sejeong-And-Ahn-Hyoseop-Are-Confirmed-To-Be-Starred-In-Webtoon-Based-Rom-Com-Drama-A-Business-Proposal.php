<h3>According to sources Kakao entertainment revealed that Kim
Sejeong is confirmed to star as the wacky and optimistic Shin
Ha Ri in SBS’s “A Business Proposal”, a drama slated to air in
the first half of 2022.<br><br>
The singer-actress revealed, “I really enjoyed reading the script,
and I felt confident that I’d be able to have fun working with the
writer, director, and fellow actors. I’m very nervous because I’m
a rom-com rookie who’s never been in an actual romantic
comedy before, but I’m working hard to prepare everything from
my appearance to my acting, so please anticipate it a lot.”</h3>
  
<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 “A Business Proposal” is a romantic comedy about an office
employee who goes on a blind date with her company’s CEO in
place of a friend while hiding her identity. The drama is also
titled as “office blind date”. The story of the drama sounds quite
interesting and fans are eagerly waiting for screening of this
fantastic drama.
  </h3><br>

<h2>Cast and Roles
</h2>
<h3>
Ahn Hyo Seop has been confirmed to play the perfect third-
generation chaebol Kang Tae Mu who goes on a date with Kim
Sejeong&#39;s Shin Ha Ri. Shin Ha Ri is a bright and optimistic young
girl, whose enthusiasm and zest for life is second to none. While
playing the role, Kim Sejeong will go back and forth between the
fake &#39;cool femme fatale&#39; persona Shin Ha Ri created to conceal
her identity and Shin Ha Ri’s quirky and energetic real self.
</h3><br>

<h2>Directors and Writers
</h2>
<h3>
The drama will be directed by Park Sun Ho of &#39;Wok of
Love&#39; and &#39;Suspicious Partner&#39; and produced by Kross
Pictures. The drama is going to be exciting under the
direction of director Park Sun Ho. Fans are eagerly waiting
for this upcoming drama.
</h3><br>


<h2>Filming and Release
</h2>
<h3>
The drama is expected to be released in first half of 2022.
Fans are excited and waiting for any further updates.
</h3>
