<h3> After giving dazzling performance in his last hit series “Start-
up”, Nam Joo-Hyuk’s fans haven’t got to see him much on the
screen even he appeared in “The school Nurse files” , but it
didn’t left much impact on the viewers. But now his fans are
too excited as it seems that he is coming again with his amazing
performance in his upcoming k-drama “Twenty Five Twenty
One”. The drama is going to be amazing as he is going to be
casted against very beautiful Kim Tae Ri. She is popular for her
roles in many shows like “Mr.Sunshine” and her latest series
“Space Sweepers” was also quite exciting.</h3>

<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 The drama will show a story which take place from 1998-2021.
In 1998, in a world that seems on the brink of disaster, two
people aged 22 and 18 meet for the first time but end up
hurting each other at the age of 23 and 19. At 24 and 20 they
learn to trust each other and at age 25 and 21 they start to fall
in love. As the story seems we will able to see many sides of
actors with different roles of different age groups.
  </h3><br>

<h2>Cast and Roles
</h2>
<h3>
Nam joo hyuk is going to play the role of main lead Baek
Yi Jin, and Kim Tae Ri is going to play female lead as Na
Hee Do. Bona will play Go Yoo Rim, an athlete of national
fencing team. Bona had done her debut in “the best hit”
and continued her acting career.
</h3><br>

<h2>Directors and Writers
</h2>
<h3>
“Twenty-five Twenty-one” is going to be written by Kwon
Do Eun who is also famous for his work in the series
“Search:WWW”. The drama will be directed by Jung Ji
Hyun who had shown amazing direction in many dramas
like “MR.Sunshine” and “The King:Eternal Monarch”.
</h3><br>


<h2>Filming and Release
</h2>
<h3>
Fans must have to be a little bit more patient as the show
is going to be release on 31 st December, 2021. This show
is going to be a huge success of 2022.
</h3>
