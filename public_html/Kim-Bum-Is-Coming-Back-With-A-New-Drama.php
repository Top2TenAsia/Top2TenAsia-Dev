 <h2> Drama Story
 </h2>
 <h3>
 Ghost doctor is going to be a fantasy medical drama about
two doctors who could not be more different in terms of
their background, skills or personalities but who end up
combining bodies and spirits. The drama will tell the
emotional stories of ghost doctors who can’t leave their
patients or hospital even after their death. The drama
highlight story of doctors who put their patients before than
anything and care for them at their fullest and can’t stop
carrying about them even after they lose their lives.
