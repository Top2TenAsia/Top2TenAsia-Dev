upcoming drama “My Liberation Diary.</p>
                                   <h3>Drama story</h3>
								<p>This drama is going to be a romantic, family drama. The
drama shows the story of three siblings and a mysterious
stranger who wants to escape their frustrating lives. Set in
Sanpo Village, a place where usually the influx of people
leaving is higher than the ones moving in. There lives the
three Yeom siblings who all want to escape from a life that
is clogged up with &#39;no answer&#39;. One day, a mysterious
man suddenly moves into their neighborhood. He is a
drunkard, degenerate, and looks like he has many
burdens and secrets. His cautious personality and
preference to remain in his own business, made him the
talk of the town, leading even the timid youngest child of
the three siblings, to approach him. </p>
                                    <h3>Cast</h3>
								<p>The drama will be starring very talented cast including Lee
Min Ki, Kim Ji Won, Lee El, Son Seok Koo, Lee Ki Woo as
their main leads and chun ho jin as their supporting lead.</p>
                                    <h3>Direction and writing</h3>
								<p>Kim seok yoon will be directing this amazing drama and
park hae young will be doing the screenwriting for the
drama “My Liberation Diary”. </p>
