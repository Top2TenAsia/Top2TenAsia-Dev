 <h3>

 If you are one of the members of the BTS Army, then you surely know about the McDonald&#39;s
special BTS meal. The fast-food joint collaborated with the global K-pop sensation to bring their
favourite meal to all the McDonald&#39;s customers across the world. The BTS-special meal was
launched recently, and in no time, it became a hit globally and even in Indonesia. ‘BTS’ the
global musical phenomenon, is not only ruling charts with its latest single ‘Butter’ , but has also
left fans in a frenzy as they try to get their hands on a BTS Meal, the band’s collaboration with
McDonald’s. However, their craze in Indonesia went to such heights that authorities had to
temporarily shut numerous outlet on Wednesday (June 9) over Covid-19 fears.In Indonesia,
while in-store eating was not allowed due to the pandemic, the meals led to huge queues outside
outlets.<br><br>
The BTS meal, which consists of 10-piece McNuggets, accompanying Cajun and sweet chilli
sauces, a medium Coca-Cola and medium fries, was first made available in Indonesia on
Wednesday (Jun 9), and the response was overwhelming.Photos of food delivery riders queueing
to fulfil online orders for the meal were shared on social media.<br><br>
Jakarta and several cities posted closure stickers on at least 13 McDonald’s outlets as they were
swamped with orders. Police in Jakarta said that a total of 32 branches were temporarily closed
across the city. There were further closures in other parts of the country. “In the Central Jakarta
area, there were 6 outlets, 6 outlets in West Jakarta, 5 outlets in North Jakarta, 9 outlets in South
Jakarta and 6 outlets in East Jakarta,” the law enforcement agency said on its website, stating
authorities found stores violating health protocols.<br><br>
We temporarily closed four of six McDonald’s stores here in Semarang for a couple of days,”
Fajar Purwoto, the city’s public order agency head, told AFP. “I don’t want Semarang to be in
the Covid-19 red zone again.”<br><br>
McDonald’s Indonesia was quick to remind ardent fans that the BTS meal will be available till
the next month, and so there was no need to “rush and worry about running out of this product”.

