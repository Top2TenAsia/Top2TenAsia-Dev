  <h3>SBS&#39;s upcoming drama series &#39;I&#39;m Breaking Up Now&#39; (literal translation) has confirmed its cast
lineup. The media outlet reported that Song Hye Kyo has been cast in the drama series along
with actress Choi Hee Seo, actor Jang Ki Yong, and Kim Joo Hun.</h3>
<br>
 <h2> Drama Story
 </h2>
 <h3>
 According to the reports the drama &#39;I&#39;m Breaking Up Now&#39; depicts the stories that occur in the
fashion industry. It tells the story of the bitterness of a breakup as it depicts the true facade of the
parting between the two people.  it will &quot;tell stories of emotional love and breakups&quot; as hinted by
the title.
  </h3><br>

  <h2>Casting And Episodes
</h2>
<h3>
Song Hye-kyo&#39;s character, Ha Young-eun will be an extension of the actress&#39; fashionable style.
Song will be playing a &quot;trendy, intelligent and realist&quot; manager of a fashion label&#39;s design team.<br><br>

 Jang Ki-yong will be playing Song&#39;s leading man. A stylish actor in his own right, Jang caught
the attention of many from his roles in Go Back Couple (2017) and My Mister (2018) which
eventually landed hun his first lead role in Come and Hug Me (2018). A series of dramas
followed including Search: WWW (2019) and Born Again (2020). This new role might just be
the rising actor&#39;s biggest break yet, playing Yoon Jae-Guk, who&#39;s described as a &quot;wealthy and
popular freelance photographer who has the brains and the looks.&quot;<br><br>

Choi Hee-seo from Mistress (2018) and Stranger 2 (2020) will be playing Hwang Chi-sook,
Ha&#39;s high schoolmate and director of the fashion label&#39;s design team.
</h3>
<h2>Director and Screen writer
</h2>
<h3>
Lee Gil-bok will be taking the director&#39;s seat, he previously helmed hit dramas such as My Love
from the Star (2013) with Jun Ji-hyun, Vagabond (2019) and Dr Romantic 2 (2020).<br><br>

The drama will be written by Je In, who previously worked on Misty (2018). <br>

