<h3>Park Hyung Sik, Han Hyo Joo and both have been confirmed to star as the lead actors in the
upcoming tvN thriller drama: Happiness
</h3>
<br><br>
  
<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 Happiness is reportedly a thriller drama in which the story revolves around new infectious
diseases that are starting to spread in a city. The drama is expected to depict the psychological
battle, fear and physical struggle that humans go through to survive the apocalypse against the
unknown. The storyline can be safely assumed, to be highly influenced by the ongoing
pandemic.

  </h3>
<h2>Casting and Roles</h2>
<h3>
<a href="https://en.wikipedia.org/wiki/Park_Hyung-sik">Park Hyung Sik’s</a> first project after getting discharged from the military. The
actor will play the character of Jang Yi Hyun who is an intelligent detective.  he is known for his
roles in The Heirs (2013), High Society (2015),  Hwarang: The Poet Warrior
Youth  (2016), Strong Girl Bong-soon (2017), and Suits (2018)<br><br>
It will be <a href="https://en.wikipedia.org/wiki/Han_Hyo-joo">Han Hyo Joo’s</a> first drama as a lead actor after W in 2016. She is best known for her
leading roles in television drama series: Spring Waltz (2006); Brilliant Legacy (2009); Dong
Yi (2010) and W (2016); as well as the film Cold Eyes (2013), for which she won Best Actress at
the 34th Blue Dragon Film Awards, and romance film The Beauty Inside (2015).<br><br>
Apart from Park Hyung Sik and Han Hyo Joo, actor <a href="https://en.wikipedia.org/wiki/Jo_Woo-jin_(actor)">Jo Woo Jin</a> has also been confirmed to be
on the cast of the upcoming drama. He will be playing the role of Han Tae Suk, a commander of
the Armed Forces Medical Command. Woo Jin’s character will hold a key to the unknown
infectious disease.<br><br>
Fans have already geared up their expectations for the return of the two lead actors on screen. It
is most likely to start filming this month.

<br><br>

</h3>

<h2>Directors and Screenwriters
</h2>
<h3>
Ahn Gil-ho and Han Sang-woon, are the director and the writer of this drama. They also worked
together in the recent hit drama ‘Sweet Home’. PD Ahn Gil Ho has produced many hit dramas
like Forest of Secrets, Memories of Alhambra, and Record of Youth .

</h3>
