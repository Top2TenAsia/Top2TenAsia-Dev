<h3> Ah Bo Hyun and Kim Go Eun are ready to come up with
an amazing drama with very unique and important
concept. Their upcoming drama “Yumi’s Cells” is a cell-
based psychological romance that unravels the daily life of
an ordinary office worker Yumi through the eyes of the
cells in her head. The story of this drama is based on a
webtoon with same title.</h3>

<br><br>
 <h2> Drama Story
 </h2>
 <h3>
 The series will take place during the strictest prohibition
law period in North Joseon&#39;s history, portraying human
aspirations and proving that prohibition is not the best
option. It is a story of three people who could damage each
other&#39;s lives by constantly colliding in front of the secret
liquor warehouse during the Joseon Prohibition Act. An
inspector who came to Seoul to attain fame and glory to
save his family name, a poor nobleman who started to
brew alcohol to pay off his debt, and the prince who keeps
crossing the wall to drink. The story is going to be amazing
with great cast working in it.
  </h3><br>

<h2>Cast and Roles
</h2>
<h3>
In this drama Kim Go-eun is going to be appear as Kim Yumi, an
ordinary office worker, Ahn Bo-hyun as Ku Woong, a game
developer, Lee Yu-bi as Ruby, Yumi&#39;s co-worker who prides
herself on mastering various dating techniques and Park Ji-
hyun as Seo Sae-i, Ku Woong&#39;s colleague and &#39;girlfriend&#39;. She
dreams of becoming the best game art director.
</h3><br>

<h2>Directors and Writers
</h2>
<h3>
“Yuni’s cells” is going to be directed by Lee Sang Yeob who
had also worked in many successful dramas and written by Kim
Kyung-ran and Kim Yoon-joo.
</h3><br>


<h2>Filming and Release
</h2>
<h3>
The drama is expected to be aired in September 17, 2021
and going to have 14 episodes in the series.
</h3>
