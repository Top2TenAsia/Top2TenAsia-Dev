 <h3>

 Here BTS have great surprise for all army out there. Finally
BTS’s hard work and their dedication set another records in front
of the world. Day by day they are achieving great success and
also prepares for better results every time.<br><br>

As we all know about BTS’s new summer single song
“BUTTER” already ruled our heart. After “DYNAMITE” from 2020
“BUTTER” is great success this year and it is just beginning since
“BUTTER” being released, but it already break a lot of records.
Recently BTS break five Guiness world record.<br><br>

According to youtube , official music video of “BUTTER” hit
3.9 million concurrent viewers for its premier on May 21,2021.
With this track they set the record for “most viewers for the
premier of a music video on YouTube”.<br><br>

BTS doesn’t did something like this first time here, previously
BTS also held this record with their single “DYNAMITE” which had
3 million concurrent peak viewers. So it is not wrong to say that
they break their own record. The global sensation holds No. 1 &amp;
No. 2 spots for biggest YouTube premiers to date.<br><br>

They also broke the record of “most viewers for premier of
(any) video on YouTube” after “BUTTER” with almost 4 million
peak concurrent .<br><br>

The break recording session doesn’t ends here. Additionally,
BUTTER has 108.2 million views in first 24 hours of its release,
surpassing BTS’s own 101.1 million view record for “DYNAMITE”.<br><br>

“BUTTER” also broke the records for “the most viewed
YouTube music video in 24 hours by a k-pop group” and “the
most viewed YouTube music video in 24 hours” by any artist.
What a proud moment for BTS and all the army out there.<br><br>

“BUTTER” also set the record for “the most streamed track on
Spotify in the first 24 hours” with 11,042,335 global streams in just
one day. It beat Ed Sheeran and Justin Bieber’s 2019 hit “I Don’t
Care”.<br><br><br>

With these latest achievements ,BTS currently holds a total of
23 Guinness World Records. And they will not stop here we all
know they are going to achieve bigger goals in future. Also in
April, BTS’s first English language track “DYNAMITE” broke
several records. “DYNAMITE” is currently the fastest Korean
group music video to achieve 1 billion views and longest charting
song in the Bilboard’s Hot 100 history.<br><br>

Very recent achievement of BTS was on Friday, May 21,
when BTS performed BUTTER first time on television . on May 24
at Billboard Music Awards 2021,also took trophy at home.<br>

