 <h2> Drama Story
 </h2>
 <h3>
 The series will take place during the strictest prohibition
law period in North Joseon&#39;s history, portraying human
aspirations and proving that prohibition is not the best
option. It is a story of three people who could damage each
other&#39;s lives by constantly colliding in front of the secret
liquor warehouse during the Joseon Prohibition Act. An
inspector who came to Seoul to attain fame and glory to
save his family name, a poor nobleman who started to
brew alcohol to pay off his debt, and the prince who keeps
crossing the wall to drink. The story is going to be amazing
with great cast working in it.
  </h3><br>

