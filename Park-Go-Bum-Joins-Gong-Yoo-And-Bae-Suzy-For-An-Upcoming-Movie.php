<h3>
<span style="color: dodgerblue">Park Bo Gum</span>’s character would be in Coma and her lady love <span style="color: dodgerblue">Bae Suzy</span> wants to interact with him. So she requests him to meet her in <span style="color: violet">Wonderland</span>.
